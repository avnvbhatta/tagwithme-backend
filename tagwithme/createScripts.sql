CREATE TABLE events
(
id VARCHAR(128) NOT NULL,
	name varchar(512) not null,
	segment varchar(128),
	genre varchar(128),
	startTime time,
	startDate date,
	images varchar(1024),
	url varchar(1024),
	venue varchar(512),
	distance varchar(128),
	address varchar(256),
	city varchar(128),
	state varchar(128),
	location varchar(128),
	parking varchar(512),
	priceRange varchar(512),
	postalCode varchar(64),
	userId varchar(512)

)






